package collection;

public class Student {
	private int age;
	private String name;
	public void setage(int age)
	{
		this.age=age;
	}
	
	public int getage()
	{
		return this.age;
	}
	public void setname(String name)
	{
		this.name=name;
	}
	public String getname()
	{
		return this.name;
	}
}
